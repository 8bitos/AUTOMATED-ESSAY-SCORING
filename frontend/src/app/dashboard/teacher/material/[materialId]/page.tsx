const containsHtmlTag = (value?: string): boolean => /<([a-z][\w-]*)\b[^>]*>/i.test(value || "");

const escapeHtml = (value: string): string =>
  value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");

const materialBlocksToRichHtml = (blocks: MaterialContentBlock[]): string => {
  const items = blocks
    .map((block) => {
      const align = block.align && block.align !== "left" ? ` style="text-align:${block.align};"` : "";
      if (block.type === "heading") {
        return `<h2${align}>${escapeHtml(block.value)}</h2>`;
      }
      if (block.type === "paragraph") {
        return `<p${align}>${escapeHtml(block.value).replace(/\n/g, "<br/>")}</p>`;
      }
      if (block.type === "bullet_list" || block.type === "number_list") {
        const rows = block.value
          .split("\n")
          .map((r) => r.trim())
          .filter(Boolean)
          .map((r) => `<li>${escapeHtml(r)}</li>`)
          .join("");
        if (!rows) return "";
        return block.type === "bullet_list" ? `<ul${align}>${rows}</ul>` : `<ol${align}>${rows}</ol>`;
      }
      if (["link", "pdf", "ppt", "video", "image"].includes(block.type) && block.value.trim()) {
        const safeUrl = escapeHtml(block.value.trim());
        return `<p${align}><a href="${safeUrl}" target="_blank" rel="noopener noreferrer">${safeUrl}</a></p>`;
      }
      return "";
    })
    .filter(Boolean);

  if (items.length === 0) return "<p></p>";
  return items.join("\n");
};

const normalizeMaterialForRichEditor = (raw?: string, fileUrl?: string, sectionCardId?: string): string => {
  const sectionCards = parseSectionCards(raw);
  if (sectionCards) {
    const selected = sectionCardId ? sectionCards.find((c) => c.id === sectionCardId) : null;
    const selectedBody = (selected?.body || "").trim();
    if (selectedBody) {
      if (containsHtmlTag(selectedBody)) return selectedBody;
      return `<p>${escapeHtml(selectedBody).replace(/\n/g, "<br/>")}</p>`;
    }
    return "<p></p>";
  }

  const blocks = parseMaterialBlocks(raw);
  if (blocks && blocks.length > 0) {
    return materialBlocksToRichHtml(blocks);
  }

  const trimmed = (raw || "").trim();
  if (trimmed) {
    if (containsHtmlTag(trimmed)) return trimmed;
    return `<p>${escapeHtml(trimmed).replace(/\n/g, "<br/>")}</p>`;
  }

  if (fileUrl?.trim()) {
    const safe = escapeHtml(fileUrl.trim());
    return `<p><a href="${safe}" target="_blank" rel="noopener noreferrer">${safe}</a></p>`;
  }

  return "<p></p>";
};

const sanitizeEditorHtml = (html: string): string => {
  const noScript = html
    .replace(/<script[\s\S]*?>[\s\S]*?<\/script>/gi, "")
    .replace(/<style[\s\S]*?>[\s\S]*?<\/style>/gi, "")
    .replace(/[\u202A-\u202E\u2066-\u2069]/g, "")
    .trim();
  return noScript || "<p></p>";
};

const cleanRichPreviewHtml = (html: string): string => {
  if (typeof document === "undefined") return sanitizeEditorHtml(html);

  const container = document.createElement("div");
  container.innerHTML = sanitizeEditorHtml(html);

  container.querySelectorAll("[data-media-delete='1'], [data-media-hint='1']").forEach((node) => node.remove());

  container.querySelectorAll<HTMLElement>("[data-media-block='1']").forEach((block) => {
    block.removeAttribute("contenteditable");
    block.style.outline = "none";
    block.style.boxShadow = "none";
    block.style.resize = "none";
    block.style.overflow = "visible";
    block.style.border = "0";
    block.style.padding = "0";
    block.style.background = "transparent";
    block.style.minHeight = "";

    block.querySelectorAll<HTMLElement>("iframe, video, embed, object, a").forEach((el) => {
      el.style.pointerEvents = "";
    });
  });

  return sanitizeEditorHtml(container.innerHTML);
};

const normalizeEmbedUrl = (url: string): string => {
  const trimmed = (url || "").trim();
  if (!trimmed) return "";
  const ytMatch = trimmed.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([^&?/]+)/i);
  if (ytMatch?.[1]) return `https://www.youtube.com/embed/${ytMatch[1]}`;
  const ytShortsMatch = trimmed.match(/youtube\.com\/shorts\/([^&?/]+)/i);
  if (ytShortsMatch?.[1]) return `https://www.youtube.com/embed/${ytShortsMatch[1]}`;
  return trimmed;
};

const resolveQuestionIdsForSectionCard = (
  cards: SectionCardItem[],
  targetCardId: string,
  questions: EssayQuestion[]
): string[] => {
  if (!targetCardId || !Array.isArray(cards) || cards.length === 0) return [];
  const soalCards = cards.filter((card) => card.type === "soal");
  if (soalCards.length === 0) return [];

  const questionById = new Map(questions.map((q) => [q.id, q]));
  const resolvedByCardId = new Map<string, string[]>();
  const usedIds = new Set<string>();

  soalCards.forEach((card) => {
    const explicitIds = Array.isArray(card.meta?.question_ids)
      ? card.meta.question_ids.filter((id): id is string => typeof id === "string" && id.trim().length > 0)
      : [];
    const validIds = explicitIds.filter((id) => questionById.has(id));
    if (validIds.length === 0) return;
    resolvedByCardId.set(card.id, validIds);
    validIds.forEach((id) => usedIds.add(id));
  });

  const fallbackQueue = questions.filter((q) => {
    if (usedIds.has(q.id)) return false;
    const rawKeywords = typeof q.keywords === "string" ? q.keywords : "";
    const keywords = rawKeywords.split(",").map((keyword) => keyword.trim().toLowerCase()).filter(Boolean);
    return !keywords.includes("tugas_submission");
  });
  soalCards.forEach((card) => {
    if (resolvedByCardId.has(card.id)) return;
    const fallback = fallbackQueue.shift();
    if (!fallback?.id) return;
    resolvedByCardId.set(card.id, [fallback.id]);
    usedIds.add(fallback.id);
  });

  return resolvedByCardId.get(targetCardId) || [];
};

