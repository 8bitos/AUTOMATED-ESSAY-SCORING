const DEFAULT_ROUNDING_STEP = 5;
const SECTION_REFERENCE_PREFIX = "__section__:";
const CARD_REFERENCE_PREFIX = "__card__:";

const normalizeRoundingStep = (value: unknown): number => {
  const n = Number(value);
  if (!Number.isFinite(n) || n <= 0) return DEFAULT_ROUNDING_STEP;
  return n;
};

const roundToStep = (value: number, step: number): number => {
  const safeStep = normalizeRoundingStep(step);
  return Math.round(value / safeStep) * safeStep;
};

const formatNumeric = (value: number): string => {
  const rounded = Math.round(value * 100) / 100;
  return Number.isInteger(rounded) ? String(rounded) : rounded.toString();
};

const getErrorMessage = (err: unknown, fallback: string): string => {
  if (err instanceof Error && err.message.trim()) return err.message;
  return fallback;
};

const getBodyMessage = (body: unknown): string | undefined => {
  if (typeof body !== "object" || body === null) return undefined;
  const record = body as { message?: unknown };
  return typeof record.message === "string" && record.message.trim() ? record.message : undefined;
};

const buildRAGReferenceOptions = (material: MaterialDetail | null): RAGReferenceOption[] => {
  if (!material?.id) return [];
  const options: RAGReferenceOption[] = [
    {
      value: `${SECTION_REFERENCE_PREFIX}${material.id}`,
      label: `SECTION: ${material.judul || "Tanpa Judul"}`,
      refType: "section",
      materialId: material.id,
    },
  ];
  const cards = parseSectionCards(material.isi_materi);
  if (!cards) return options;
  cards
    .filter((card) => card.type === "materi")
    .forEach((card, idx) => {
      const cardTitle = (card.title || "").trim() || `Materi ${idx + 1}`;
      options.push({
        value: `${CARD_REFERENCE_PREFIX}${card.id}`,
        label: cardTitle,
        refType: "card",
        materialId: material.id,
        sectionCardId: card.id,
      });
    });
  return options;
};

const buildLocalRAGPreview = (material: MaterialDetail | null, option: RAGReferenceOption | null): string => {
  if (!material || !option) return "";
  if (option.refType === "section") {
    const cards = parseSectionCards(material.isi_materi);
    if (cards && cards.length > 0) {
      const merged = cards
        .filter((c) => c.type === "materi")
        .map((c) => stripHtmlTags((c.body || "").trim()))
        .filter(Boolean)
        .join("\n");
      return merged.slice(0, 320);
    }
    return stripHtmlTags((material.isi_materi || "").trim()).slice(0, 320);
  }
  const cards = parseSectionCards(material.isi_materi);
  if (!cards) return "";
  const target = cards.find((c) => c.id === option.sectionCardId && c.type === "materi");
  if (!target) return "";
  const body = stripHtmlTags((target.body || "").trim());
  return body.slice(0, 320);
};

const formatDescriptor = (value: unknown) => {
  if (value == null) return "";
  if (typeof value === "string" || typeof value === "number") return String(value);
  if (typeof value === "object") {
    const descriptor = value as RubricDescriptorObject;
    if (typeof descriptor.description === "string") return descriptor.description;
    if (typeof descriptor.deskripsi === "string") return descriptor.deskripsi;
    return JSON.stringify(value);
  }
  return String(value);
};

const renderDoubleAsteriskBold = (text?: string): JSX.Element[] => {
  const safeText = String(text || "");
  const lines = safeText.split("\n");
  return lines.map((line, lineIndex) => {
    const parts = line.split(/(\*\*[^*]+\*\*)/g);
    return (
      <span key={`line-${lineIndex}`}>
        {parts.map((part, partIndex) => {
          const isBoldToken = part.startsWith("**") && part.endsWith("**") && part.length > 4;
          if (isBoldToken) {
            return <strong key={`part-${lineIndex}-${partIndex}`}>{part.slice(2, -2)}</strong>;
          }
          return <span key={`part-${lineIndex}-${partIndex}`}>{part}</span>;
        })}
        {lineIndex < lines.length - 1 && <br />}
      </span>
    );
  });
};

const createBlockId = () => `blk_${Math.random().toString(36).slice(2, 9)}`;

const parseMaterialBlocks = (raw?: string): MaterialContentBlock[] | null => {
  if (!raw) return null;
  try {
    const parsed = JSON.parse(raw);
    if (parsed?.format !== "sage_blocks" || !Array.isArray(parsed?.blocks)) return null;
    return parsed.blocks
      .map((b: Record<string, unknown>) => ({
        id: typeof b.id === "string" && b.id ? b.id : createBlockId(),
        type: b.type as MaterialBlockType,
        value: typeof b.value === "string" ? b.value : "",
        align:
          typeof b.align === "string" && ["left", "center", "right", "justify"].includes(b.align)
            ? b.align
            : "left",
        size:
          typeof b.size === "string" && ["small", "medium", "large", "full"].includes(b.size)
            ? b.size
            : "medium",
      }))
      .filter((b: MaterialContentBlock) => ["heading", "paragraph", "video", "image", "link", "pdf", "ppt", "bullet_list", "number_list"].includes(b.type));
  } catch {
    return null;
  }
};

const parseSectionCards = (raw?: string): SectionCardItem[] | null => {
  if (!raw) return null;
  try {
    const parsed = JSON.parse(raw) as { format?: string; items?: unknown[] };
    if (parsed?.format !== "sage_section_cards_v1" || !Array.isArray(parsed?.items)) return null;
    return parsed.items
      .map((x: unknown) => {
        if (typeof x !== "object" || x === null) return null;
        const row = x as Record<string, unknown>;
        if (typeof row.id !== "string") return null;
        return {
          id: row.id,
          type: typeof row.type === "string" ? row.type : "materi",
          title: typeof row.title === "string" ? row.title : "",
          body: typeof row.body === "string" ? row.body : "",
          created_at: typeof row.created_at === "string" ? row.created_at : undefined,
          meta: typeof row.meta === "object" && row.meta !== null ? (row.meta as Record<string, unknown>) : undefined,
        } as SectionCardItem;
      })
      .filter((x): x is SectionCardItem => x !== null);
  } catch {
    return null;
  }
};

